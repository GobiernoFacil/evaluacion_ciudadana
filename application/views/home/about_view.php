			<h1>¿Qué es <strong>Tú Evalúas</strong>?</h1>
			<p><strong>Tú Evalúas</strong> es una plataforma que habilita a los beneficiarios de los programas del 
				<strong>Gobierno de la República</strong> para dar su opinión respecto de su experiencia e identificar 
				oportunidades de mejora para los encargados de los mismos. La plataforma permite a los encargados de los 
				programas cargar cuestionarios respecto de sus programas, y a los beneficiarios expresar su opinión, 
				a cual se refleja en tiempo real de manera sencilla, atractiva y disponible al público en formato de datos abiertos.</p>
